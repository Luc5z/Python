# Introdução a Loops

for numero in range(1, 6): # imprimir de 1 até 5 (1: start | 6: stop)
    print(numero)

palavra = 'Google'

for letra in palavra: # vai percorrer cada index da string e fazer o que estiver no loop
        print(f'{letra} esta na palavra {palavra}') # Formated strings é uma maravilha


# Só aceitar produto se o valor for entre 20 e 40 (verificar se um valor está entre 2 valores)

valor = 20


if valor >= 20 and valor < 40: # --> Jeito paia <--
    print('Produto aceito')
else:
    print('Produto negado')


if 20 <= valor < 40: # --> Jeito mais organizado e limpo
    print('Produto aceito')
else:
    print('Produto negado')

